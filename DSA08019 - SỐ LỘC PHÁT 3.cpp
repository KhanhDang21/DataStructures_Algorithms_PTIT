int main()
{
	int t;
	cin >> t;
	while(t--)
	{
		int n;
		cin >> n;
		stack<string> st;
		queue<string> q;
		q.push("6");
		q.push("8");
		while(q.front().size() <= n)
		{
			string tmp = q.front();
			st.push(tmp);
			q.pop();
			q.push(tmp + "6");
			q.push(tmp + "8");
		}
		cout << st.size() << endl;
		while(!st.empty())
		{
			cout << st.top() << " ";
			st.pop();
		}
		cout << endl;
	}
	return 0;
}
